#include <stdio.h>

int main() {
	printf("size of an int = %d\n", sizeof (int));
	printf("size of an long int = %d\n", sizeof (long int));
	printf("size of an long long int = %d\n", sizeof (long long int));
	printf("size of an float = %d\n", sizeof (float));
	printf("size of an double = %d\n", sizeof (double));
	printf("size of an long double = %d\n", sizeof (long double));
	printf("size of an char = %d\n", sizeof (char));
	
	printf("size of an unsigned int = %d\n", sizeof (unsigned int));
	printf("size of an unsigned long long int = %d\n", sizeof (unsigned long long int));
	printf("size of an unsigned char = %d\n", sizeof (unsigned char));
	
	
	printf("size of an short int = %d\n", sizeof (short int));
	
	return 0;
}

